#include "WinMTRParams.h"

//*****************************************************************************
// WinMTRParams::WinMTRParams
//
//*****************************************************************************

WinMTRParams::WinMTRParams()
	: reportToFile(FALSE)
{
}

//*****************************************************************************
// WinMTRParams::SetHostName
//
//*****************************************************************************
void WinMTRParams::SetHostName(const char *host)
{
	_snprintf(hostname, SIZE_HOSTNAME, "%s", host);
}

//*****************************************************************************
// WinMTRParams::SetCycles
//
//*****************************************************************************
void WinMTRParams::SetCycles(int c)
{
	cycles = c;
}

//*****************************************************************************
// WinMTRParams::SetInterval
//
//*****************************************************************************
void WinMTRParams::SetInterval(float i)
{
	interval = i;
}

//*****************************************************************************
// WinMTRParams::SetPingSize
//
//*****************************************************************************
void WinMTRParams::SetPingSize(int ps)
{
	pingsize = ps;
}

//*****************************************************************************
// WinMTRParams::SetTimeout
//
//*****************************************************************************
void WinMTRParams::SetTimeout(float t)
{
	timeout = t;
}

//*****************************************************************************
// WinMTRParams::SetReport
//
//*****************************************************************************
void WinMTRParams::SetReport(bool b)
{
	report = b;
}

//*****************************************************************************
// WinMTRParams::SetUseDNS
//
//*****************************************************************************
void WinMTRParams::SetUseDNS(bool udns)
{
	useDNS = udns;
}

//*****************************************************************************
// WinMTRParams::SetWide
//
//*****************************************************************************
void WinMTRParams::SetWide(bool w)
{
	wide = w;
}

//*****************************************************************************
// WinMTRParams::SetFormat
//
//*****************************************************************************
void WinMTRParams::SetFields(const char* f)
{
	_snprintf(fields, SIZE_FIELDS, "%s", f);
}

//*****************************************************************************
// WinMTRParams::SetFilename
//
//*****************************************************************************
void WinMTRParams::SetFilename(const char *f)
{
	reportToFile = TRUE;
	_snprintf(filename, SIZE_FILENAME, "%s", f);
}
